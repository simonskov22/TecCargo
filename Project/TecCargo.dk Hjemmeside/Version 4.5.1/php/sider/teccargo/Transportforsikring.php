        <p>
            <span class="spanBlueBoldText">Hvad dækkes?</span><br>
            <br>
            Forsikringen er tegnet på udvidede danske betingelser og starter, når fragtmanden afhenter godset og<br>
            ophører ved aflevering til modtager. Disse forsikringer dækker næsten enhver beskadigelse af varerne<br>
            under transport, herunder også bortkomst og tyveri.<br>
            <br>
            Undtagelserne gælder skader som følge af varens egen beskaffenhed, manglende emballage, dårlig<br>
            stuvning af afsender, forsinkelse og uegnet transportmiddel.<br>
            <br>
            Synlige skader anmeldes senest syv dage efter modtagelsen. <br>
            <br>
            <span class="spanBlueBoldText">Hvornår dækkes?</span><br>
            <br>
            Dækningen følger som hovedregel leveringsbetingelserne, f.eks. INCOTERMS, som har regler om, hvem der<br>
            bærer risikoen eller har forsikringspligten. Forsikringen træder i kraft, når godset er afhentet på<br>
            afsendelsesstedet og vedvarer under transportens almindelige forløb, indtil varen er ankommet hos<br>
            modtagerens pakhus eller lager, til distribution eller det for forsikringen gældende bestemmelsessted.<br>
            <br>
            <span class="spanBlueBoldText">Hvor meget dækkes?</span><br>
            <br>
            Ved totalskade udbetales forsikringssummen, ellers dækkes omkostningerne ved den nødvendige<br>
            reparation. Forsikringssummen indgår i varens fakturapris, heri fragt, forsikringspræmie samt evt.<br>
            deklareret avance.<br>
            <br>
            <span class="spanBlueBoldText">Forsikring Pr. fragtbrev</span><br>
            <br>
            Betyder, at man forsikrer en enkelt forsendelse. Altså at varerne til én enkelt modtager forsikres.<br>
