        <p>
            <span class="spanBlueBoldText">Hvorfor forsikre varer under transport?</span><br>
            <br>
            Varetransport vil altid være forbundet med en vis risiko. Det kan desværre ikke undgås, at nogle varer<br> 
            undertiden beskadiges eller går fuldstændigt tabt. De internationale regler <br>
            <br>
            begrænser transportørens ansvar, således at denne kan være helt fri <br>
            for ansvar for skader på varerne, og såfremt der er et ansvar, kan selve<br>
            erstatningsydelsen minimeres efter gældende regler.<br>
            <br>
            Ved at forsikre varen under transporten, sikrer man sig mod et økonomisk <br>
            tab, da transportørens ansvar ikke kan forventes at dække her.<br>
            <br>
            <span class="spanBlueBoldText">Hvornår forsikring?</span><br>
            <br>
            Det er dog langt fra i alle tilfælde, at en ekstra forsikring er påkrævet. Det afhænger af varernes værdi.<br>
            Hvis værdien er mindre end 8,33 SDR pr. kg., dækker TECcargos forsikring automatisk i tilfælde af skade<br>
            på forsendelserne.<br>
            <br>
            <span class="spanBlueBoldText">Hvad er SDR?</span><br>
            <br>
            Special Drawing Rights (SDR) er ikke en valuta, som vi kender den. Det er en særlig trækningsrettighed,<br>
            som må bruges af den internationale valutafond (IMF). Special Drawing Rights er defineret som et vægtet<br>
            sum af bidrag på fire større valutaer, som revurderes og justeres hvert 5. år og beregnes dagligt i form af<br>
            tilsvarende amerikanske dollars
        </p>
            
