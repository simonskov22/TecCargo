            <h3>Modtagerens ansvar</h3>
            <p>
                Modtageren skal modtage godset uden udsættelse, med mindre der er tvingende årsager til ikke at modtage det.<br>
                Når godset er modtaget, skal modtageren sikre sig, at de regler, der omfatter ham, er overholdt.
            </p>
            <p>Modtageren har ansvaret for at:</p>
            <ul>
                <li>overholde kravet om en sikkerhedsrådgiver</li>
                <li>foretage den forskrevne rengøring af køretøjet i de tilfælde, hvor det er bestemt i ADR</li>
                <li>sikre sig, at containere ikke har faremærkning, når de er tomme og rensede</li>
            </ul>
            <p>
                Hvis undersøgelser viser fejl eller mangler i henhold til ADR, må modtageren ikke returnere containeren, før disse<br>
                fejl eller mangler er rettet.
