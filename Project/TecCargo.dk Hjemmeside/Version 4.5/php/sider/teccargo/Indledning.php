            <h2>Emballering</h2>
            <p>
                Godset skal være forsvarligt emballeret (indpakket), så det kan modstå almindelig påvirkning<br>
                ved transport i presenningsopbyggede biler. Paller og stykgods skal kunne tåle håndtering med<br>
                truck og lignende. Emballeringen skal dels sikre godset mod beskadigelse og dels sikre, at godset<br>
                ikke forvolder skade på personer, materiel eller andet gods. Kunden er ansvarlig i henhold til<br>
