                pligter skal der reklameres straks. Reklamationen skal altid ske skriftligt til FairKurer og<br>
                indeholde en beskrivelse af skaden. Modtageren skal på modtagers adresse stille beskadiget gods<br>
                til disposition for TECcargo med henblik på besigtigelse heraf. TECcargo har endvidere ret til at<br>
                kræve det beskadigede gods udleveret med henblik på besigtigelse.
            </p>
            
            <h2>Erstatning</h2>
            <p>
                Erstatning for hel eller delvis bortkomst af godset beregnes efter godsets værdi på stedet og<br>
                tidspunktet for overtagelsen til befordring. Erstatningen kan dog ikke overstige SDR 8,33 pr. kg<br>
                for enkelt forsendelser. Ved beskadigelse af godset skal fragtføreren erstatte værdiforringelsen.<br>
                Erstatningen kan dog ikke overstige det beløb, som skulle have været betalt, hvis det<br>
                beskadigede gods var gået tabt. Erstatning for forsinkelse forudsætter, at modtager kan<br>
                dokumentere, at forsinkelsen har medført tab. Ved forsinkelse erstattes<br>
                maksimalt fragtbeløbet, medmindre der er indgået særskilt aftale ved særlig interesse jævnfør<br>
                CMR-lovens § 33. Erstatningskrav skal rejses skriftligt, være dokumenteret og sendes til<br>
                TECcargo. Såfremt TECcargo har betalt fuld erstatning, overgår ejendomsretten til TECcargo.<br>
                Nægter kunden at udlevere godset, bortfalder retten til erstatning.
            </p>
            
            <h2>Forældelse</h2>
