                overfor kunden med hensyn til emballering. Eventuel godkendelse fratager ikke kunden for<br>
                ansvar eller medfører ansvar for TECcargo.
            </p>
            
            <h2>Udelukket fra transport</h2>
            <p>
                TECcargo transporterer ikke afdøde menneskelegemer (lig) og dyr (levende eller døde).<br>
                Efterkrav udføres ikke. Ildelugtende gods samt gods eller indpakning, der er stærkt tilsmudset,<br>
                modtages ikke til transport.
            </p>
            
            <h2>Betaling</h2>
