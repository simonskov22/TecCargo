                Afvisningen kan fremsendes pr. e-mail.<br>
                Forældelsesfristen afbrydes ved indlevering af stævning, ved indgåelse af skriftlig aftale om<br>
                suspension af forældelsesfristen eller ved TECcargo anerkendelse af ansvar.
