                forældelsesfristen fra udløbet af en frist på tre måneder efter fragtaftalens indgåelse.<br>
                Forældelsesfristen for krav mod TECcargo suspenderes, når skriftligt krav fra den berettigede er<br>
                kommet frem til TECcargo. Forældelsesfristen løber igen, når TECcargo skriftligt afviser kravet.<br>
                Kopier af<br>
