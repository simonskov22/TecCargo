            <h2>Reklamation</h2>
            <p>
                Ved synlig beskadigelse eller bortkomst skal reklamation ske straks ved godsets modtagelse.<br>
                Dette gælder både skader på gods og skader på emballage, og reklamationen skal angives på<br>
                fragtbrevet. Ved skjult (ikke synlig) beskadigelse eller bortkomst skal modtageren reklamere<br>
                skriftligt inden syv dage (søn- og helligdage ikke medregnet) efter godsets aflevering. Ved<br>
                forsinkelse skal reklamation ske inden 21 dage (søn- og helligdage medregnet), ellers kan der<br>
